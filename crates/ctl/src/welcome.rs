/// Show welcome screen. No animation, no colors. Just the logo.
pub fn run_welcome(_ebpf_hooks: u32) {
    let mut out = io::stdout();

    let sword_width = SWORDS.iter().map(|l| l.chars().count()).max().unwrap_or(80);
    let title_width = TITLE[0].chars().count();

    println!();

    // Swords
    for line in SWORDS {
        println!("{line}");
    }

    println!();

    // Block title ‚ÄĒ centered under swords
    let title_pad = sword_width.saturating_sub(title_width) / 2;
    for line in TITLE {
        println!("{}{line}", " ".repeat(title_pad));
    }

    println!();

    out.flush().unwrap();
}
